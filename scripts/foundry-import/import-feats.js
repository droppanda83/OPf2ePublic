function writeReport(feats) {
  const categoryCounts = {};
  const implCounts = {};
  const sourceCounts = {};
  const levelCounts = {};

  for (const f of feats) {
    categoryCounts[f.category] = (categoryCounts[f.category] || 0) + 1;
    implCounts[f.implemented] = (implCounts[f.implemented] || 0) + 1;
    if (f.source) {
      sourceCounts[f.source] = (sourceCounts[f.source] || 0) + 1;
    }
    levelCounts[f.level] = (levelCounts[f.level] || 0) + 1;
  }

  const report = {
    generatedAt: new Date().toISOString(),
    sourceFile: path.relative(ROOT, SOURCE_FILE).replace(/\\/g, '/'),
    outputFiles: Object.fromEntries(
      Object.entries(OUTPUT_FILES).map(([k, v]) => [k, path.relative(ROOT, v).replace(/\\/g, '/')])
    ),
    totalFeats: feats.length,
    meetsPhase13Target: feats.length >= MIN_ANCESTRY_COUNT + MIN_GENERAL_COUNT + MIN_SKILL_COUNT,
    categoryCounts,
    implCounts,
    sourceCounts,
    levelCounts,
  };

  const genDir = path.dirname(REPORT_FILE);
  if (!fs.existsSync(genDir)) {
    fs.mkdirSync(genDir, { recursive: true });
  }

  fs.writeFileSync(REPORT_FILE, JSON.stringify(report, null, 2));
  return report;
}

function run() {
  const source = readSource();
  assert(Array.isArray(source.feats), 'Expected source.feats to be an array');

  // Validate all
  const seen = new Set();
  source.feats.forEach((f, i) => {
    validateFeat(f, i);
    assert(!seen.has(f.id), `Duplicate feat id '${f.id}'`);
    seen.add(f.id);
  });

  // Split by category
  const ancestry = source.feats.filter((f) => f.category === 'ancestry');
  const general = source.feats.filter((f) => f.category === 'general');
  const skill = source.feats.filter((f) => f.category === 'skill');

  assert(ancestry.length >= MIN_ANCESTRY_COUNT, `Expected at least ${MIN_ANCESTRY_COUNT} ancestry feats, found ${ancestry.length}`);
  assert(general.length >= MIN_GENERAL_COUNT, `Expected at least ${MIN_GENERAL_COUNT} general feats, found ${general.length}`);
  assert(skill.length >= MIN_SKILL_COUNT, `Expected at least ${MIN_SKILL_COUNT} skill feats, found ${skill.length}`);

  // Generate output files
  fs.writeFileSync(OUTPUT_FILES.ancestry, renderAncestryFeats(ancestry));
  fs.writeFileSync(OUTPUT_FILES.general, renderGeneralFeats(general));
  fs.writeFileSync(OUTPUT_FILES.skill, renderSkillFeats(skill));

  const report = writeReport(source.feats);
  console.log(`[foundry-import] Imported ${report.totalFeats} feats`);
  console.log(`[foundry-import]   Ancestry: ${ancestry.length}, General: ${general.length}, Skill: ${skill.length}`);
  console.log(`[foundry-import]   Implementation: `, report.implCounts);
}

if (require.main === module) {
  run();
}

module.exports = { run };
